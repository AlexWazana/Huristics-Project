        self.lbl_best2.setText(str(bot_3_idx[1]))
        self.lbl_best3.setText(str(bot_3_idx[0]))
        total_time = datetime.now() - start
        seconds = total_time.microseconds
        seconds = (seconds/10000)
        self.lbl_runtime.setText(str(seconds))

