        my_data = genfromtxt('Normalized.csv', delimiter=',', skip_header=1)
        for i in range(20):
            current_weight = weight[i]
            for j in range(48):
                my_data[j][i] = (my_data[j][i]*int(current_weight))
        maxInColumns=np.amax(my_data,axis=0)
        minInColumns=np.amin(my_data,axis=0)
        for i in range(48):
            x = 0
            y = 0
            for j in range(20):
                x = x + ((my_data[i][j] - maxInColumns[j])*(my_data[i][j] - maxInColumns[j]))
                y = y + ((my_data[i][j] - minInColumns[j])*(my_data[i][j] - minInColumns[j]))
            x=math.sqrt(x)
            y=math.sqrt(y)
            my_data[i][20] = y / (x + y)
        tempArray = my_data[:,20]
        bot_3_idx=np.argsort(tempArray)[-48:]
        self.lbl_worst1.setText(str(bot_3_idx[45]))
